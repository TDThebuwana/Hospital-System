    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id=pid.getText();
        String name=pname.getText();
        String address=paddress.getText();
        int age=Integer.parseInt(page.getText());
        String gender=pgender.getSelectedItem().toString();
        String conno=pconno.getText();
        String ward=pward.getSelectedItem().toString();
        
        controller ct = new controller();
        String doc=ct.getIDbyName(pdoc.getSelectedItem().toString(), "doctor");
        
        Patient pt = new Patient();
        pt.EditPatient(id, name, address, age, gender, conno, ward, doc);
    }//GEN-LAST:event_jButton1ActionPerformed

