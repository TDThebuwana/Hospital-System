    private void pnameMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pnameMousePressed
         try {
        
            if(!pid.getText().isEmpty()){
                
                paddress.enable();
                page.enable();
                pgender.enable();
                pconno.enable();
                pward.enable();
                pdoc.enable();
           
                String pid = this.pid.getText();
                
                controller ct = new controller();
                ResultSet rst=ct.getDetailsbyDB(pid, "patient");
                
                while(rst.next()){                
                    
                    pname.setText(rst.getString(2));
                    paddress.setText(rst.getString(3));
                    page.setText(rst.getString(4));
                    pgender.setSelectedItem(rst.getString(5));
                    pconno.setText(rst.getString(6));
                    pward.setSelectedItem(rst.getString(7));
                    pdoc.setSelectedItem(rst.getString(8));
                    
                    
                    
                    
                }
            } 
            }catch (SQLException ex) {
                Logger.getLogger(EditPatient.class.getName()).log(Level.SEVERE, null, ex);
            }
        
    }//GEN-LAST:event_pnameMousePressed

    private void pidMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pidMousePressed
        try {
        
            if(!pname.getText().isEmpty()){
                
                paddress.enable();
                page.enable();
                pgender.enable();
                pconno.enable();
                pward.enable();
                pdoc.enable();
           
                controller ct = new controller();
                
                String pid = ct.getIDbyName(this.pname.getText(), "patient");
                
                
                ResultSet rst=ct.getDetailsbyDB(pid, "patient");
                
                while(rst.next()){                
                    this.pid.setText(pid);
                    pname.setText(rst.getString(2));
                    paddress.setText(rst.getString(3));
                    page.setText(rst.getString(4));
                    pgender.setSelectedItem(rst.getString(5));
                    pconno.setText(rst.getString(6));
                    pward.setSelectedItem(rst.getString(7));
                    pdoc.setSelectedItem(rst.getString(8));
                    
                    
                    
                    
                }
            } 
            }catch (SQLException ex) {
                Logger.getLogger(EditPatient.class.getName()).log(Level.SEVERE, null, ex);
            }
        
    }//GEN-LAST:event_pidMousePressed

